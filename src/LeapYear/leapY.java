    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(jTextField1.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Input First !", "Warning", JOptionPane.WARNING_MESSAGE);
            }
            else{
                boolean tf = new ageCal().isLeap(Integer.parseInt(jTextField1.getText()));
                if (tf==true){
                    JOptionPane.showMessageDialog(null, jTextField1.getText()+"- is LeapYear.", "Message", JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    JOptionPane.showMessageDialog(null, jTextField1.getText()+"- is Not LeapYear.", "Message", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jTextField1KeyPressed

