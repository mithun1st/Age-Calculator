    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Input First !", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            boolean tf = new ageCal().isLeap(Integer.parseInt(jTextField1.getText()));
            if (tf==true){
                JOptionPane.showMessageDialog(null, jTextField1.getText()+"- is LeapYear.", "Message", JOptionPane.CLOSED_OPTION);
            }
            else{
                JOptionPane.showMessageDialog(null, jTextField1.getText()+"- is Not LeapYear.", "Message", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

